package com.spring.ejercicioGet.model;

public class ListaSalteada {
    private int desde;
    private int hasta;
    private int salto;

    //getters y setters
    public int getDesde() {
        return desde;
    }
    public void setDesde(int desde) {
        this.desde = desde;
    }
    public int getHasta() {
        return hasta;
    }
    public void setHasta(int hasta) {
        this.hasta = hasta;
    }
    public int getSalto() {
        return salto;
    }
    public void setSalto(int salto) {
        this.salto = salto;
    }
}
